function drawSingleTimeBasedCloud(cloud) {
  push();
  translate(cloud.x, cloud.y);
  scale(cloud.scale);

  // 绘制预渲染纹理。tint 只控制整朵云透明度，不改变它内部的高光和阴影层次。
  // Draw the pre-rendered texture. tint controls only the whole cloud opacity without flattening its internal highlights and shadows.
  imageMode(CENTER);
  tint(255, cloud.alpha);
  image(cloud.texture, 0, 0);
  noTint();

  pop();
}

// Tapered stroke technique: samples a Bézier curve using bezierPoint() at small intervals,
// then draws each segment with a strokeWeight modulated by sin(PI * t) — thin at both ends,
// thicker in the middle — to mimic the calligraphic quality of ukiyo-e ink brushwork.
// Uses p5.js bezierPoint() reference: https://p5js.org/reference/p5/bezierPoint/
function drawTaperedWingStroke(x1, y1, cx1, cy1, cx2, cy2, x2, y2, alpha, baseWeight = 2.2) {
  // 用分段线模拟笔触粗细变化：两端细，中段略粗，更接近参考图中的手绘海鸥线条。
  // Simulate changing brush thickness with segmented lines: thin ends and a thicker middle, closer to the hand-drawn gull strokes in the reference.
  const steps = 12;
  strokeCap(ROUND);
  strokeJoin(ROUND);

  for (let i = 0; i < steps; i++) {
    const t1 = i / steps;
    const t2 = (i + 1) / steps;
    const mid = (t1 + t2) * 0.5;
    const taper = sin(PI * mid);
    const xA = bezierPoint(x1, cx1, cx2, x2, t1);
    const yA = bezierPoint(y1, cy1, cy2, y2, t1);
    const xB = bezierPoint(x1, cx1, cx2, x2, t2);
    const yB = bezierPoint(y1, cy1, cy2, y2, t2);

    strokeWeight(baseWeight * (0.38 + taper * 0.82));
    stroke(32, 24, 20, alpha * (0.72 + taper * 0.28));
    line(xA, yA, xB, yB);
  }
}

function drawSmallBodyStroke(alpha, bodyTilt = 0) {
  // 参考图里的海鸥不是完整鸟身，而是中心几笔短线暗示身体、头部和翼根。
  // In the reference, the gull body is not fully drawn; a few short central strokes suggest body, head, and wing root.
  push();
  rotate(bodyTilt);
  stroke(32, 24, 20, alpha * 0.82);
  strokeWeight(1.25);
  strokeCap(ROUND);
  line(-4, 1, 5, 1);
  strokeWeight(0.95);
  pop();
}

function drawSingleTimeBasedBird(bird, alpha, groupPhase) {
  const wingPhase = groupPhase * bird.wingSpeed + bird.phaseOffset;
  const flap = sin(wingPhase);
  const wingLift = map(flap, -1, 1, -2.5, 3.5);

  // Individual drift: each bird uses its phaseOffset as a unique Perlin seed so they
  // wander independently while still following the flock. Kept small so formation holds.
  const driftX = (noise(bird.phaseOffset * 4,       groupPhase * 0.28) - 0.5) * 14;
  const driftY = (noise(bird.phaseOffset * 4 + 100, groupPhase * 0.65) - 0.5) * 40;

  push();
  translate(bird.ox + driftX, bird.oy + driftY + sin(wingPhase * 0.35) * 2);
  scale(bird.scale);

  // 参考图风格：远景海鸥由多段有粗细变化的深色笔触构成，不画实体鸟身。
  // Reference style: distant seagulls are built from several dark tapered strokes, without solid bodies.
  noFill();

  if (bird.variant === 1) {
    // variant 1 是较窄的姿态，翅膀长度与当前 scale 一起缩放，保持大小关系一致。
    // Variant 1 is a narrower pose; wing length scales with the bird's current scale, keeping proportions consistent.
    drawTaperedWingStroke(-24, 5, -17, -2 - wingLift, -8, -7 - wingLift, 0, 0, alpha, 1.85);
    drawTaperedWingStroke(0, 0, 7, -7 - wingLift, 17, -5 - wingLift, 27, 3, alpha, 1.75);
    drawSmallBodyStroke(alpha, radians(-4));
  } else if (bird.variant === 2) {
    // variant 2 模拟参考图中一侧较平、一侧上扬的自然姿态。
    // Variant 2 imitates the reference's natural pose with one flatter wing and one lifted wing.
    drawTaperedWingStroke(-27, 4, -18, 1 - wingLift, -8, -1 - wingLift, 0, 0, alpha, 1.95);
    drawTaperedWingStroke(0, 0, 8, -10 - wingLift, 20, -12 - wingLift, 32, -1, alpha, 1.9);
    drawSmallBodyStroke(alpha, radians(3));
  } else {
    // 默认姿态使用更长的两侧翅膀主笔触，并添加短副笔触，让轮廓更像参考图。
    // The default pose uses longer main wing strokes plus short secondary strokes, making the outline closer to the reference.
    drawTaperedWingStroke(-30, 5, -20, -6 - wingLift, -8, -9 - wingLift, 0, 0, alpha, 2.15);
    drawTaperedWingStroke(0, 0, 9, -9 - wingLift, 22, -8 - wingLift, 34, 3, alpha, 2.05);
    drawSmallBodyStroke(alpha, 0);
  }

  pop();
}

function drawTimeBasedBirds() {
  const artworkScale = max(width / artworkWidth, height / artworkHeight);
  const artworkOffsetX = (width - artworkWidth * artworkScale) / 2;
  const artworkOffsetY = (height - artworkHeight * artworkScale) / 2;

  push();
  translate(artworkOffsetX, artworkOffsetY);
  scale(artworkScale);

  push();
  translate(birdFlockX, birdFlockY);
  for (const bird of timeBasedBirdFlock) {
    drawSingleTimeBasedBird(bird, 205, birdFlockTimer);
  }
  pop();

  pop();
}

function drawTimeBasedClouds() {
  // 云朵使用设计坐标系绘制，和海浪/船一样会随窗口等比例缩放。
  // Clouds are drawn in the design coordinate system, so they scale with the window like waves and boats.
  const artworkScale = max(width / artworkWidth, height / artworkHeight);
  const artworkOffsetX = (width - artworkWidth * artworkScale) / 2;
  const artworkOffsetY = (height - artworkHeight * artworkScale) / 2;

  push();
  translate(artworkOffsetX, artworkOffsetY);
  scale(artworkScale);

  for (const cloud of timeBasedClouds) {
    drawSingleTimeBasedCloud(cloud);
  }

  pop();
}

function updateMechanicTime() {
  // deltaTime 是 p5 提供的上一帧到当前帧的毫秒数。
  // deltaTime is provided by p5 and stores the milliseconds elapsed since the previous frame.
  //
  // min(deltaTime, 50) 防止页面卡顿后动画突然跳太远。
  // min(deltaTime, 50) prevents a huge animation jump after a browser pause or lag spike.
  const deltaSeconds = min(deltaTime, 50) / 1000;

  // 按真实时间推进全局时间。0.36 是整体动画速度倍率。
  // Advance global time using real elapsed time. 0.36 is the overall animation speed multiplier.
  noiseTime += deltaSeconds * 0.36;

  // 返回秒数，方便其他 time-based mechanic 使用同一份时间步长。
  // Return seconds so other time-based mechanics can use the same time step.
  return deltaSeconds;
}

function updateTimeDrivenBackgroundLines(deltaSeconds, currentTime) {
  for (const backgroundLine of backgroundLines) {
    // 背景浪按方向和速度上下移动，移动量由真实经过时间决定。
    // Move the background wave vertically using direction, speed, and real elapsed time.
    backgroundLine.y += backgroundLine.dir * backgroundLine.speed * deltaSeconds;

    // 到达下边界 300 后反向向上。
    // Reverse upward after reaching the lower boundary at y = 300.
    if (backgroundLine.y >= 300) {
      backgroundLine.y = 300;
      backgroundLine.dir = -1;
    }

    // 到达上边界 290 后反向向下。
    // Reverse downward after reaching the upper boundary at y = 290.
    if (backgroundLine.y <= 290) {
      backgroundLine.y = 290;
      backgroundLine.dir = 1;
    }

    // offset 记录相对于 baseY 的偏移，绘制时用 baseY + offset 得到实际位置。
    // offset stores the displacement from baseY; drawing uses baseY + offset as the actual position.
    backgroundLine.offset = backgroundLine.y - backgroundLine.baseY;

    // 保存当前时间和偏移，用于之后重画历史状态，形成拖影。
    // Save the current time and offset so older states can be redrawn as a trail.
    backgroundLine.history.push({ offset: backgroundLine.offset, time: currentTime });

    // 超过拖影长度时删除最旧记录，避免数组无限增长。
    // Remove the oldest record once the trail is too long, preventing unbounded array growth.
    if (backgroundLine.history.length > backgroundTrailLength) backgroundLine.history.shift();
  }
}

function drawBoat(x, y, boatWidth) {
  // 船的所有尺寸都从 boatWidth 推导，这样大船小船可以共用同一个函数。
  // All boat dimensions are derived from boatWidth, so large and small boats can share this function.
  const boatHeight = boatWidth * 0.24;
  const left = x - boatWidth / 2;
  const right = x + boatWidth / 2;
  const tipY = y - boatHeight * 0.12;
  const bottomLeftX = left + boatWidth * 0.18;
  const bottomRightX = right - boatWidth * 0.18;
  const bottomY = y + boatHeight * 0.55;

  const drawHull = () => {
    // cornerCut 控制船身两侧收进去的程度。
    // cornerCut controls how much the hull corners are pulled inward.
    const cornerCut = 0.28;

    beginShape();
    vertex(left, tipY);
    vertex(lerp(left, bottomLeftX, 1 - cornerCut), lerp(tipY, bottomY, 1 - cornerCut));

    // quadraticVertex 画弯曲船底，让船身不像硬折线。
    // quadraticVertex draws a curved hull bottom so the boat does not look like hard straight segments.
    quadraticVertex(bottomLeftX, bottomY, lerp(bottomLeftX, bottomRightX, cornerCut), bottomY);
    vertex(lerp(bottomLeftX, bottomRightX, 1 - cornerCut), bottomY);
    quadraticVertex(bottomRightX, bottomY, lerp(bottomRightX, right, cornerCut), lerp(bottomY, tipY, cornerCut));
    vertex(right, tipY);
    quadraticVertex(x, y + boatHeight * 0.18, left, tipY);
    endShape(CLOSE);
  };

  noStroke();

  // 船身基础木色。
  // Base wood color for the hull.
  const hullBaseColor = color('#B98A55');

  // 根据 steps 生成更暗的木色，用于画出船身层次。
  // Generate darker wood colors from steps to create layered shading on the hull.
  const shadeOf = (steps) => lerpColor(hullBaseColor, color(0), steps * 0.18);

  const bandEdgesAt = (t) => ({
    // t 是从船顶到底部的比例，用来找某条横向木板分界线的位置。
    // t is the proportion from the hull top to bottom, used to locate a horizontal plank boundary.
    bandY: lerp(tipY, bottomY, t),
    leftX: lerp(left, bottomLeftX, t),
    rightX: lerp(right, bottomRightX, t),
  });

  const fillAboveBand = ({ bandY, leftX, rightX }) => {
    // 只填充分界线以上的船身区域，用多次覆盖形成明暗层。
    // Fill only the area above a boundary line; repeated overlays create light and dark layers.
    beginShape();
    vertex(left, tipY);
    vertex(leftX, bandY);
    quadraticVertex(x, bandY + boatHeight * 0.08, rightX, bandY);
    vertex(right, tipY);
    quadraticVertex(x, y + boatHeight * 0.18, left, tipY);
    endShape(CLOSE);
  };

  const passengerCount = boatWidth >= 200 ? 4 : 2;
  const passengerSpacing = boatWidth * 0.145;
  const passengerStartX = x - passengerSpacing * (passengerCount - 1) / 2;
  const deckY = tipY + boatHeight * 0.12;
  const personScale = boatWidth * 0.0085;

  const drawPassengers = () => {
    for (let i = 0; i < passengerCount; i++) {
      const px = passengerStartX + passengerSpacing * i;
      const lean = i % 2 === 0 ? -0.20 : 0.12;
      const robeColor = i % 2 === 0 ? color(38, 42, 54) : color(72, 49, 34);

      push();
      translate(px, deckY);
      scale(personScale);
      rotate(lean);

      // 头部先画，帽子后画以覆盖在上方。
      stroke(62, 41, 24, 185);
      strokeWeight(0.03);
      fill(42, 29, 22);
      ellipse(0, -5.6, 5.0, 4.2);

      // 版画式斗笠。
      stroke(62, 41, 24, 185);
      strokeWeight(0.75);
      fill(218, 190, 128);
      beginShape();
      vertex(-6.8, -6.0);
      quadraticVertex(0, -4.5, 6.8, -6.0);
      vertex(0, -11.2);
      endShape(CLOSE);

      // 身体。
      stroke(28, 24, 22, 180);
      strokeWeight(0.8);
      fill(robeColor);
      beginShape();
      vertex(-3.6, -5.0);
      quadraticVertex(1.4, -3.0, 3.8, 1.2);
      vertex(1.6, 6.0);
      vertex(-3.4, 5.4);
      quadraticVertex(-5.0, 0.0, -3.6, -5.0);
      endShape(CLOSE);

      pop();
    }
  };

  const drawPassengerArms = () => {
    // 手臂和桨绕身体端点摆动，模拟划船动作。
    // Arms and oars rotate around their body-end pivot to simulate rowing.
    for (let i = 0; i < passengerCount; i++) {
      const px = passengerStartX + passengerSpacing * i;
      const lean = i % 2 === 0 ? -0.20 : 0.12;
      const rowAngle = sin(noiseTime * 2.5 + i * 1.5) * 0.35;

      push();
      translate(px, deckY);
      scale(personScale);
      rotate(lean);

      stroke(38, 26, 18, 165);

      // Arm pivots at body-end; oar is nested at the arm tip so it stays connected.
      push();
      translate(2.2, -1.8);
      rotate(rowAngle);
      strokeWeight(0.75);
      line(0, 0, 5.3, 4.6);
      // Oar starts exactly at the arm tip.
      strokeWeight(0.95);
      line(5.3, 4.6, 5.3 + 7.4, 4.6 + 6.8);
      pop();

      pop();
    }
  };

  // 人物先画，之后船身的颜色层会盖住下半身，只露出帽子和头部，符合浮世绘远景效果。
  // Passengers are drawn first; the boat hull layers painted on top cover their lower bodies,
  // leaving only hats and heads visible above the rim — matching the ukiyo-e distant-figure style.
  drawPassengers();

  // 从深到浅依次覆盖，形成木船的体积感。
  // Paint from darker to lighter overlays to give the wooden boat volume.
  fill(shadeOf(3));
  drawHull();

  fill(shadeOf(2));
  fillAboveBand(bandEdgesAt(3 / 4));

  fill(shadeOf(1));
  fillAboveBand(bandEdgesAt(2 / 4));

  fill(shadeOf(0));
  fillAboveBand(bandEdgesAt(1 / 4));

  // 最后加描边，明确船的轮廓。
  // Add the final outline to clarify the boat shape.
  stroke('#5E3F22');
  strokeWeight(0.5);
  noFill();
  drawHull();

  // 手臂和桨画在最后，确保它们显示在船身上方。
  // Arms and oars drawn last so they appear on top of the boat hull.
  drawPassengerArms();
}

function drawTimeDrivenBoat(baseX, baseY, boatWidth, direction = 1, speed = 1, verticalRange = 18) {
  // boatPhase 是船的动画相位。它由 noiseTime 推进，speed 可以让单独某条船浮动得更快或更慢。
  // boatPhase is the animation phase for the boat. It is driven by noiseTime, and speed lets an individual boat float faster or slower.
  const boatPhase = noiseTime * 2 * speed;

  push();

  // cos 控制左右漂移，sin 控制上下漂浮，漂浮幅度由 verticalRange 决定。direction 用来让不同船只运动方向有差异。
  // cos controls horizontal drifting, while sin controls vertical bobbing whose amplitude is set by verticalRange. direction makes different boats move differently.
  translate(baseX + cos(boatPhase * 0.5) * 55 * direction, baseY + sin(boatPhase) * verticalRange * direction);

  // 让船随时间轻微倾斜，模拟被浪托起和放下的感觉。
  // Slightly rotate the boat over time to suggest it being lifted and lowered by waves.
  rotate(sin(boatPhase) * radians(5) * direction);

  // 船本体的形状和材质由上面的 drawBoat() 负责绘制。
  // The boat's shape and material are drawn by drawBoat() above.
  drawBoat(0, 0, boatWidth);

  pop();
}

function drawTimeDrivenSunRings(centerX, sunCenterY, sunRadius) {
  noStroke();

  // sunGlowMultiplier (from audio_mechanic.js) makes rings expand further and pulse brighter with audio.
  const audioBoost = typeof sunGlowMultiplier !== 'undefined' ? sunGlowMultiplier : 1.0;
  const maxScale = map(audioBoost, 0.7, 3.0, 3.0, 5.5, true);
  const alphaMult = map(audioBoost, 0.7, 3.0, 0.7, 2.0, true);

  // 三个光环错开时间扩散，形成连续的太阳脉冲效果。
  // Three rings expand with staggered timing, creating a continuous solar pulse effect.
  for (let ring = 0; ring < 3; ring++) {
    // progress 在 0 到 1 之间循环，代表当前光环的生命周期。
    // progress loops from 0 to 1 and represents the current ring's life cycle.
    const progress = (noiseTime * 0.3 + ring / 3) % 1;

    // 光环从太阳半径扩展到 maxScale 倍，音频越强扩得越大。
    // The ring expands from the sun radius to maxScale times that radius; louder audio = larger expansion.
    const ringRadius = lerp(sunRadius, sunRadius * maxScale, progress);

    // progress 越大透明度越低，使光环扩散后逐渐消失。
    // The alpha decreases as progress grows, making each expanding ring fade away.
    const baseAlpha = pow(constrain(1 - progress * 3, 0, 1), 3) * 220;
    fill(232, 77, 28, min(baseAlpha * alphaMult, 255));
    circle(centerX, sunCenterY, ringRadius * 2);
  }
}

function drawTimeDrivenSunPulse(centerX, sunCenterY, sunRadius) {
  // sin 产生平滑循环值，让太阳本体轻微放大缩小。
  // sin creates a smooth looping value, making the sun gently grow and shrink.
  const pulse = 1 + 0.06 * (sin(noiseTime * 2) * 0.5 + 0.5);

  fill(232, 77, 28);
  circle(centerX, sunCenterY, sunRadius * 2 * pulse);
}

function drawSun(centerX, sunCenterY, sunRadius) {
  // 太阳由三层叠加而成：Perlin noise 控制光芒闪烁，时间驱动光环扩散和本体脉冲。
  // The sun is layered from three effects: Perlin noise drives the ray flicker, while time drives the expanding rings and the body's pulse.
  drawPerlinSunRays(centerX, sunCenterY, sunRadius, noiseTime);
  drawTimeDrivenSunRings(centerX, sunCenterY, sunRadius);
  drawTimeDrivenSunPulse(centerX, sunCenterY, sunRadius);
}

function drawMountainAndSun() {
  // 山和太阳放在画面右侧，作为远景。两者共享同一个锚点：山顶的 y 位置 peakY。
  // The mountain and sun sit on the right side as background scenery. Both are positioned relative to one shared anchor: the mountain peak's y-position, peakY.
  const centerX = width * 0.75;
  const peakY = height * 0.42;

  // 太阳的尺寸和位置只依赖 peakY 和自身半径，与山体尺寸无关。
  // The sun's size and position depend only on peakY and its own radius — independent of the mountain's body dimensions.
  const sunRadius = height * 0.16;
  const sunCenterY = peakY - sunRadius * 0.6;

  // 太阳的实际绘制（光芒、光环、脉冲）由上面的 drawSun() 负责。
  // The sun's actual drawing (rays, rings, pulse) is handled by drawSun() above.
  drawSun(centerX, sunCenterY, sunRadius);

  // 这些变量决定富士山山顶位置和山体比例。
  // These variables define the mountain peak position and body proportions.
  const mountainBaseY = height * 0.82;
  const mountainHalfWidth = height * 0.50;
  const mountainHeight = mountainBaseY - peakY;
  const baseHalfWidth = mountainHalfWidth + mountainHalfWidth * (height - mountainBaseY) / (mountainBaseY - peakY);
  const craterHalfWidth = mountainHalfWidth * 0.10;
  const craterDepth = mountainHeight * 0.08;

  // 绘制富士山主体。
  // Draw the main body of Mount Fuji.
  fill(35, 65, 100);
  beginShape();
  vertex(centerX - baseHalfWidth, height);
  vertex(centerX - craterHalfWidth, peakY);
  quadraticVertex(centerX, peakY + craterDepth, centerX + craterHalfWidth, peakY);
  vertex(centerX + baseHalfWidth, height);
  endShape(CLOSE);

  // 山顶雪线的位置和波纹参数。
  // Position and ripple parameters for the snowy mountain top.
  const snowBaseY = peakY + mountainHeight * 0.28;
  const snowHalfWidth = craterHalfWidth + (baseHalfWidth - craterHalfWidth) * (snowBaseY - peakY) / (height - peakY);
  const rippleAmp = mountainHeight * 0.040;
  const rippleFreq = PI * 4 / snowHalfWidth;
  const rippleStep = snowHalfWidth / 50;

  // 绘制白色雪顶，底部用 sin 做起伏边缘。
  // Draw the white snowy top, using sin to create a wavy lower edge.
  fill(242, 247, 255);
  beginShape();
  vertex(centerX - craterHalfWidth, peakY);
  quadraticVertex(centerX, peakY + craterDepth, centerX + craterHalfWidth, peakY);
  vertex(centerX + snowHalfWidth, snowBaseY);
  for (let dx = snowHalfWidth; dx >= -snowHalfWidth; dx -= rippleStep) {
    vertex(centerX + dx, snowBaseY + rippleAmp * abs(sin(dx * rippleFreq)));
  }
  vertex(centerX - snowHalfWidth, snowBaseY);
  endShape(CLOSE);

  // 给雪顶左侧加半透明阴影，让山更有立体感。
  // Add a translucent shadow to the left side of the snow cap to make the mountain feel more dimensional.
  fill(195, 215, 235, 150);
  beginShape();
  vertex(centerX - craterHalfWidth, peakY);
  quadraticVertex(centerX - craterHalfWidth / 2, peakY + craterDepth / 2, centerX, peakY + craterDepth / 2);
  vertex(centerX, snowBaseY);
  for (let dx = 0; dx >= -snowHalfWidth; dx -= rippleStep) {
    vertex(centerX + dx, snowBaseY + rippleAmp * abs(sin(dx * rippleFreq)));
  }
  vertex(centerX - snowHalfWidth, snowBaseY);
  endShape(CLOSE);
}
